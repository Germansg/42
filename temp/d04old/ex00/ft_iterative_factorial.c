int	ft_iterative_factorial(int nb)
{
	int i;
	int j;

	j = 1;
	i = 1;
	if (nb < 0 || nb == '\0')
	{
		return (0);
	}
	while (i < nb)
	{
		i++;
		j = j * i;
	}
	return (j);
}
